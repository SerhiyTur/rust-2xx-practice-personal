#[test]
fn test() {
    const SIZE: i32 = 5; // Константа визначає половину висоти ромба (висота верхньої частини)

    // Верхня частина ромба
    for i in 0..=SIZE {
        for _ in 0..(SIZE - i) {
            print!(" "); // Виводимо пробіли
        }
        for _ in 0..(2 * i + 1) {
            print!("*"); // Виводимо зірочки
        }
        println!(); // Переходимо на новий рядок
    }

    // Нижня частина ромба
    for i in (0..SIZE).rev() {
        for _ in 0..(SIZE - i) {
            print!(" "); // Виводимо пробіли
        }
        for _ in 0..(2 * i + 1) {
            print!("*"); // Виводимо зірочки
        }
        println!(); // Переходимо на новий рядок
    }
}
